#!/bin/bash
set -e

echo ">>> Running Alembic migrations..."
alembic upgrade head

echo ">>> Starting server..."
exec gunicorn app.main:app \
    --worker-class uvicorn.workers.UvicornWorker \
    --workers 4 \
    --bind 0.0.0.0:8000 \
    --timeout 120 \
    --access-logfile - \
    --error-logfile -
